package com.github.tux2323.pooldemo;

public class ProfilingEvent {

    private String id;
    
    private Integer time;

    public void setId(String id) {
	this.id = id;
    }

    public String getId() {
	return id;
    }

    public void setTime(Integer time) {
	this.time = time;
    }

    public Integer getTime() {
	return time;
    }
    

}
